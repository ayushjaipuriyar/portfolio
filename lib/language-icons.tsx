/**
 * Get devicon class for a given technology/language name
 */
export function getLanguageIcon(name: string): string {
  // Try exact match first
  if (languageIcons[name]) {
    return languageIcons[name];
  }

  // Try case-insensitive match
  const lowerName = name.toLowerCase();
  const matchedKey = Object.keys(languageIcons).find((key) => key.toLowerCase() === lowerName);

  if (matchedKey) {
    return languageIcons[matchedKey];
  }

  // Return default icon (generic code icon)
  return 'devicon-devicon-plain';
}
